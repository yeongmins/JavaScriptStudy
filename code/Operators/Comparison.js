// 비교(Comparison)

const a = 1
const b = 3

// 동등(형 변환!)
console.log(a == b) // false

// 부등 (형 변환!)
console.log(a != b) // true

// 일치
console.log(a === b) // false

// 불일치
console.log(a !== b) // true

// 큼
console.log(a > b) // false

// 크거나 같음
console.log(a >= b) // false

// 작음
console.log(a < b) // true

// 작거나 같음
console.log(a <= b) // true